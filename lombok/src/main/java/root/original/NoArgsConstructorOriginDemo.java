public class NoArgsConstructorOriginDemo {

    private String foo;
    private String bar;

    public NoArgsConstructorOriginDemo() {
    }

    @Override
    public String toString() {
        return "NoArgsConstructorOriginDemo{" +
                "foo='" + foo + '\'' +
                ", bar='" + bar + '\'' +
                '}';
    }
}
